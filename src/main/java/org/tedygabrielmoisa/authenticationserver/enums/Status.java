package org.tedygabrielmoisa.authenticationserver.enums;

/**
 * Enum representing the status of a user account in the authentication server.
 */
public enum Status {
  /**
   * Indicates that the user account is active.
   */
  ACTIVE,

  /**
   * Indicates that the user account is inactive.
   */
  INACTIVE,

  /**
   * Indicates that the user account is suspended.
   */
  SUSPENDED,

  /**
   * Indicates that the user account is deleted.
   */
  DELETED
}
